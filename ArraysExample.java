public class ArraysExample {
    public static void main(String[] args) {

        int[] numbers = {3, 5, 7, 9, 11};

        // print array elements using a loop
        for (int i = 0; i < numbers.length; i++) {
            System.out.println("Element at index " + i + ": " + numbers[i]);
        }
    }
}
